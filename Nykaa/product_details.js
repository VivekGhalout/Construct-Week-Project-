function displayproducts(products) {

    document.getElementById("proside").innerHTML = "";

    products.map(function (ele, index) {

        var card = document.createElement("div");
        card.setAttribute("id", "card");

        var image = document.createElement("img");
        image.setAttribute("src", ele.image_url);

        var pro_name = document.createElement("p");
        pro_name.setAttribute("id", "pro_name");
        pro_name.textContent = ele.pro_name;

        var price_div = document.createElement("div");
        price_div.setAttribute("id", "price_div");

        var strikeprice = document.createElement("p");
        strikeprice.setAttribute("id", "sprice")
        strikeprice.textContent = "MRP: ₹"+ ele.strikeoffprice;

        var price = document.createElement("span");
        price.setAttribute("id", "price");
        price.textContent = "₹" + ele.price + " |";

        var off = document.createElement("span");
        off.setAttribute("id", "off");
        off.textContent = ele.off + "% Off";

        price_div.append(strikeprice, price, off);

        var enj_msg = document.createElement("p");
        enj_msg.setAttribute("id", "enj_msg");
        enj_msg.textContent = ele.messege;

        var rating = document.createElement("span");
        rating.setAttribute("id", "rating");
        rating.textContent = ele.rating;

        var a = document.createElement("a");
        a.setAttribute("href", "pro_discription.html");

        var button = document.createElement("button");
        button.setAttribute("id", "pro_info");
        button.textContent = "Product Preview";
        button.addEventListener("click", function (){
            pro_details(ele);
        });

        a.append(button);

        card.append(image, pro_name, price_div, enj_msg, rating, a);

        document.getElementById("proside").append(card);
    })
}

